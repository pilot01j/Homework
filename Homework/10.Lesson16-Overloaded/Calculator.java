public class Calculator {
    public int add (int a,int b){return a+b;}
    public long add (long a,long b){return a+b;}
    public float add (float a,float b){return a+b;}
    public double add (double a,double b){return a+b;}
    
    public int reduction(int a,int b){return a-b;}
    public long reduction(long a,long b){return a-b;}
    public float reduction(float a,float b){return a-b;}
    public double reduction (double a,double b){return a-b;}
    
    public int multiplying(int a,int b){return a*b;}
    public long multiplying(long a,long b){return a*b;}
    public float multiplying(float a,float b){return a*b;}
    public double multiplying (double a,double b){return a*b;}
    
    public int division(int a,int b){return a/b;}
    public long division(long a,long b){return a/b;}
    public float division(float a,float b){return a/b;}
    public double division (double a,double b){return a/b;}
    
    public int percent(int a,int b){return ((b*100)/a);}
    public long percent(long a,long b){return ((b*100)/a);}
    public float percent(float a,float b){return ((b*100)/a);}
    public double percent (double a,double b){return ((b*100)/a);}  
}



  
        
	
		
  
    

